    <h5><b>Pada proyek mana KPBU dapat diterapkan ?</b></h5><br>
    <p>KPBU dapat diterapkan pada semua proyek, baik yang merupakan kewenangan daerah, gabungan beberapa daerah (regional), maupun nasional.</p>
    <p  style="text-align: justify;">Secara regulasi tidak ada batas minimum nilai investasi proyek, tetapi sebaiknya tidak kurang dari Rp200 Milyar, atas pertimbangan aspek economies of scale yaitu sumber daya yang dikeluarkan pada tahapan-tahapan KPBU untuk proyek bernilai investasi rendah maupun tinggi akan sama besarnya.</p>
